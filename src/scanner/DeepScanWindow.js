class DeepScanWindow {
    constructor(srcMiner) {
        this.srcMiner = srcMiner;
        this.scanData = null;
        this.scanResults = {};
        this.isScanRunning = false;
        this.isPaused = false;
        this.currentDepth = 0;
        this.scannedUrls = new Set();
        this.pendingUrls = new Set();
        this.maxDepth = 2;
        this.concurrency = 8;
        this.timeout = 5000;
    }

    // createdeep scanÁ™óÂè£
    async createDeepScanWindow(config) {
        //console.log('üîç [DEBUG] startcreatedeep scanÁ™óÂè£Ôºåconfiguration:', config);
        
        let baseUrl = '';
        let sourceUrl = '';
        let pageTitle = '';
        try {
            const [tab] = await chrome.tabs.query({ active: true, currentWindow: true });
            //console.log('üîç [DEBUG] ÂΩìbeforeÊ†áÁ≠æpage:', tab);
            if (tab && tab.url) {
                baseUrl = new URL(tab.url).origin;
                sourceUrl = tab.url; // completeÊ∫êURL
                pageTitle = tab.title || '';
                //console.log('üîç [DEBUG] Ëß£ÊûêÂæótobaseUrl:', baseUrl);
                //console.log('üîç [DEBUG] Ëß£ÊûêÂæótosourceUrl:', sourceUrl);
            }
        } catch (error) {
            console.error('‚ùå [DEBUG] getÂΩìbeforepageÈù¢URLfailed:', error);
        }

        // ÂáÜÂ§áscanconfigurationdata
        const scanConfig = {
            maxDepth: config.maxDepth || 2,
            concurrency: config.concurrency || 8,
            timeout: config.timeout || 5000,
            scanJsFiles: config.scanJsFiles !== false,
            scanHtmlFiles: config.scanHtmlFiles !== false,
            scanApiFiles: config.scanApiFiles !== false,
            baseUrl: baseUrl,
            sourceUrl: sourceUrl, // addcompleteÊ∫êURL
            pageTitle: pageTitle, // addpageÈù¢Ê†áÈ¢ò
            initialResults: this.srcMiner.results || {},
            timestamp: Date.now()
        };

        //console.log('üîç [DEBUG] ÂáÜÂ§á‰øùÂ≠òscanconfiguration:', scanConfig);
        console.log('üîç [DEBUG] initialresultÊï∞ÈáèÁªüËÆ°:', {
            absoluteApis: scanConfig.initialResults.absoluteApis?.length || 0,
            domains: scanConfig.initialResults.domains?.length || 0,
            emails: scanConfig.initialResults.emails?.length || 0,
            jsFiles: scanConfig.initialResults.jsFiles?.length || 0
        });

        // Â∞Üconfiguration‰øùÂ≠òtoIndexedDBÔºå‰æõscanÁ™óÂè£read
        try {
            //console.log('üîç [DEBUG] start‰øùÂ≠òconfigurationtoIndexedDB...');
            await window.IndexedDBManager.saveDeepScanState(baseUrl, scanConfig);
            //console.log('‚úÖ [DEBUG] deep scanconfigurationalready‰øùÂ≠òtoIndexedDB');
            
            // validation‰øùÂ≠òÊòØÂê¶success
            const verification = await window.IndexedDBManager.loadDeepScanState(baseUrl);
            //console.log('üîç [DEBUG] validation‰øùÂ≠òresult:', verification ? 'success' : 'failed');
            
        } catch (error) {
            console.error('‚ùå [DEBUG] ‰øùÂ≠òdeep scanconfigurationfailed:', error);
            throw new Error('‰øùÂ≠òdeep scanconfigurationfailed: ' + error.message);
        }

        try {
            // useÊâ©Â±ïdeep scanpageÈù¢
            const scanPageUrl = chrome.runtime.getURL('deep-scan-window.html');
            
            // opennewÁ™óÂè£
            const newWindow = await chrome.windows.create({
                url: scanPageUrl,
                type: 'normal',
                width: 800,
                height: 900,
                focused: true
            });

            //console.log('deep scanÁ™óÂè£alreadycreate:', newWindow.id);
            return newWindow;
        } catch (error) {
            console.error('createdeep scanÁ™óÂè£failed:', error);
            throw error;
        }
    }

    // startdeep scanÔºàfromÊâ©Â±ïpageÈù¢Ë∞ÉforÔºâ
    async startDeepScan() {
        if (this.srcMiner.deepScanRunning) {
            //console.log('deep scanalreadyinËøêË°åin');
            return;
        }

        //console.log('startdeep scanÁ™óÂè£...');

        // getconfigurationparameter
        const maxDepthInput = document.getElementById('maxDepth');
        const concurrencyInput = document.getElementById('concurrency');
        const timeoutInput = document.getElementById('timeout');
        const scanJsFilesInput = document.getElementById('scanJsFiles');
        const scanHtmlFilesInput = document.getElementById('scanHtmlFiles');
        const scanApiFilesInput = document.getElementById('scanApiFiles');

        const config = {
            maxDepth: parseInt(maxDepthInput?.value) || 2,
            concurrency: parseInt(concurrencyInput?.value) || 8,
            timeout: parseInt(timeoutInput?.value) || 5,
            scanJsFiles: scanJsFilesInput ? scanJsFilesInput.checked : true,
            scanHtmlFiles: scanHtmlFilesInput ? scanHtmlFilesInput.checked : true,
            scanApiFiles: scanApiFilesInput ? scanApiFilesInput.checked : true
        };

        try {
            // Ê†áËÆ∞scanstart
            this.srcMiner.deepScanRunning = true;
            
            // Êõ¥newUIstate
            const deepScanBtn = document.getElementById('deepScanBtn');
            const configDiv = document.getElementById('deepScanConfig');
            
            if (deepScanBtn) {
                const deepScanBtnText = deepScanBtn.querySelector('.text');
                if (deepScanBtnText) {
                    deepScanBtnText.textContent = '‚èπÔ∏è ÂÅúÊ≠¢scan';
                }
                deepScanBtn.style.background = 'rgba(239, 68, 68, 0.3)';
            }

            if (configDiv) {
                configDiv.style.display = 'none';
            }

            // createdeep scanÁ™óÂè£
            await this.createDeepScanWindow(config);

        } catch (error) {
            console.error('‚ùå startdeep scanfailed:', error);
            this.srcMiner.deepScanRunning = false;
            
            // ÊÅ¢Â§çUIstate
            const deepScanBtn = document.getElementById('deepScanBtn');
            if (deepScanBtn) {
                const deepScanBtnText = deepScanBtn.querySelector('.text');
                if (deepScanBtnText) {
                    deepScanBtnText.textContent = 'deepÈÄíÂΩíscan';
                }
                deepScanBtn.style.background = '';
            }
            
            throw error;
        }
    }

    // ÂÅúÊ≠¢deep scan
    stopDeepScan() {
        this.srcMiner.deepScanRunning = false;
        this.isScanRunning = false;
        
        // notifyscanÁ™óÂè£ÂÅúÊ≠¢
        chrome.runtime.sendMessage({
            action: 'stopDeepScan'
        });

        // Êõ¥newUIstate
        const deepScanBtn = document.getElementById('deepScanBtn');
        if (deepScanBtn) {
            const deepScanBtnText = deepScanBtn.querySelector('.text');
            if (deepScanBtnText) {
                deepScanBtnText.textContent = 'deepÈÄíÂΩíscan';
            }
            deepScanBtn.style.background = '';
        }

        const configDiv = document.getElementById('deepScanConfig');
        if (configDiv) {
            configDiv.style.display = 'none';
        }
    }

    // Â§ÑÁêÜfromscanÁ™óÂè£message
    handleScanWindowMessage(message, sender, sendResponse) {
        switch (message.action) {
            case 'updateScanResults':
                this.updateMainPageResults(message.data);
                sendResponse({ success: true });
                break;
                
            case 'scanProgress':
                this.updateScanProgress(message.data);
                sendResponse({ success: true });
                break;
                
            case 'scanComplete':
                this.handleScanComplete(message.data);
                sendResponse({ success: true });
                break;
                
            case 'scanError':
                this.handleScanError(message.data);
                sendResponse({ success: true });
                break;
                
            default:
                sendResponse({ success: false, error: 'Unknown action' });
        }
    }

    // Êõ¥new‰∏ªpageÈù¢scanresult
    updateMainPageResults(newResults) {
        if (!newResults) return;

        // Âêàandresultto‰∏ªpageÈù¢
        Object.keys(newResults).forEach(key => {
            if (!this.srcMiner.results[key]) {
                this.srcMiner.results[key] = [];
            }

            // useSetËøõË°åÂéªÈáç
            const existingSet = new Set(this.srcMiner.results[key]);
            newResults[key].forEach(item => {
                if (item && !existingSet.has(item)) {
                    this.srcMiner.results[key].push(item);
                }
            });
        });

        // ÂÆûÊó∂Êõ¥newÊòæÁ§∫
        this.srcMiner.displayResults();
        this.srcMiner.saveResults();

        console.log('üîÑ ‰∏ªpageÈù¢resultalreadyÊõ¥newÔºåÂΩìbeforeresultÊï∞Èáè:', 
            Object.values(this.srcMiner.results).reduce((sum, arr) => sum + (arr?.length || 0), 0));
    }

    // Êõ¥newscanprogress
    updateScanProgress(progressData) {
        const progressDiv = document.getElementById('deepScanProgress');
        if (progressDiv && progressData) {
            progressDiv.style.display = 'block';
            
            const progressText = document.getElementById('progressText');
            const progressBar = document.getElementById('progressBar');
            
            if (progressText) {
                progressText.textContent = `${progressData.stage}: ${progressData.current}/${progressData.total} (${progressData.percentage}%)`;
            }
            
            if (progressBar) {
                progressBar.style.width = `${progressData.percentage}%`;
            }
        }
    }

    // Â§ÑÁêÜscan complete
    handleScanComplete(finalResults) {
        //console.log('üéâ deepscan completeÔºÅ');
        
        // Êõ¥newÊúÄÁªàresult
        if (finalResults) {
            this.updateMainPageResults(finalResults);
        }

        // ÈáçÁΩÆstate
        this.srcMiner.deepScanRunning = false;
        this.isScanRunning = false;

        // Êõ¥newUI
        const deepScanBtn = document.getElementById('deepScanBtn');
        if (deepScanBtn) {
            const deepScanBtnText = deepScanBtn.querySelector('.text');
            if (deepScanBtnText) {
                deepScanBtnText.textContent = '‚úÖ deepscan complete';
            }
            deepScanBtn.style.background = 'rgba(0, 212, 170, 0.3)';
            
            setTimeout(() => {
                if (deepScanBtnText) {
                    deepScanBtnText.textContent = 'deepÈÄíÂΩíscan';
                }
                deepScanBtn.style.background = '';
            }, 3000);
        }

        // ÈöêËóèprogressÊù°
        const progressDiv = document.getElementById('deepScanProgress');
        if (progressDiv) {
            setTimeout(() => {
                progressDiv.style.display = 'none';
            }, 5000);
        }

        // ‰øùÂ≠òcompletestatetoIndexedDB
        const completionState = {
            deepScanComplete: true,
            deepScanCompletedAt: Date.now(),
            deepScanResultsCount: Object.values(this.srcMiner.results).reduce((sum, arr) => sum + (arr?.length || 0), 0)
        };
        
        // getÂΩìbeforepageÈù¢URLfor‰øùÂ≠òstate
        chrome.tabs.query({ active: true, currentWindow: true }, (tabs) => {
            if (tabs[0] && tabs[0].url) {
                const baseUrl = new URL(tabs[0].url).origin;
                IndexedDBManager.saveDeepScanState(baseUrl, completionState);
            }
        });
    }

    // Â§ÑÁêÜscanÈîôËØØ
    handleScanError(errorData) {
        console.error('‚ùå deep scanÂá∫Èîô:', errorData);
        
        // ÈáçÁΩÆstate
        this.srcMiner.deepScanRunning = false;
        this.isScanRunning = false;

        // Êõ¥newUI
        const deepScanBtn = document.getElementById('deepScanBtn');
        if (deepScanBtn) {
            const deepScanBtnText = deepScanBtn.querySelector('.text');
            if (deepScanBtnText) {
                deepScanBtnText.textContent = '‚ùå scanfailed';
            }
            deepScanBtn.style.background = 'rgba(239, 68, 68, 0.3)';
            
            setTimeout(() => {
                if (deepScanBtnText) {
                    deepScanBtnText.textContent = 'deepÈÄíÂΩíscan';
                }
                deepScanBtn.style.background = '';
            }, 3000);
        }
    }
}