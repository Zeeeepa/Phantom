    /**
     * scan results get of ÊúÄËøë
     */
    async getRecentScanResults(limit = 10) {
        try {
            await this.init();
            
            const transaction = this.db.transaction([this.storeName], 'readonly');
            const store = transaction.objectStore(this.storeName);
            
            return new Promise((resolve, reject) => {
                const request = store.getAll();
                
                request.onsuccess = () => {
                    const results = request.result || [];
                    // when by Èó¥Êà≥ÊéíÂ∫èÔºålatest of before Âú®
                    const sortedResults = results.sort((a, b) => {
                        const timeA = new Date(a.extractedAt || a.timestamp || 0).getTime();
                        const timeB = new Date(b.extractedAt || b.timestamp || 0).getTime();
                        return timeB - timeA;
                    });
                    
                    // return quantity limit
                    const limitedResults = sortedResults.slice(0, limit);
                    resolve(limitedResults);
                };
                
                request.onerror = () => {
                    console.error('‚ùå scan results failed get ÊúÄËøë:', request.error);
                    reject(request.error);
                };
            });
            
        } catch (error) {
            console.error('‚ùå scan results operation failed get ÊúÄËøë:', error);
            return [];
        }
    }

